package com.softtwig.intenship.VO;

public class CalculatorVO {
	
	private int sNo;  
	private String name;
	private String degree;
	private int yearOfPassing;
	private float  careerGap;
	private float hoursSpentperDay;
	private float daysSpentperWeek;
	private String startingDate;
	
	private float daysSpentperTraining;
	private float weeksSpentperTraining;
	private float monthsSpentperTraining;
	private String courseEndDate;
	
	
	public int getsNo() {
		return sNo;
	}
	public void setsNo(int sNo) {
		this.sNo = sNo;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDegree() {
		return degree;
	}
	public void setDegree(String degree) {
		this.degree = degree;
	}
	public int getYearOfPassing() {
		return yearOfPassing;
	}
	public void setYearOfPassing(int yearOfPassing) {
		this.yearOfPassing = yearOfPassing;
	}
	public float getCareerGap() {
		return careerGap;
	}
	public void setCareerGap(float careerGap) {
		this.careerGap = careerGap;
	}
	public float getHoursSpentperDay() {
		return hoursSpentperDay;
	}
	public void setHoursSpentperDay(float hoursSpentperDay) {
		this.hoursSpentperDay = hoursSpentperDay;
	}
	public float getDaysSpentperWeek() {
		return daysSpentperWeek;
	}
	public void setDaysSpentperWeek(float daysSpentperWeek) {
		this.daysSpentperWeek = daysSpentperWeek;
	}
	public String getStartingDate() {
		return startingDate;
	}
	public void setStartingDate(String startingDate) {
		this.startingDate = startingDate;
	}
	public float getDaysSpentperTraining() {
		return daysSpentperTraining;
	}
	public void setDaysSpentperTraining(float daysSpentperTraining) {
		this.daysSpentperTraining = daysSpentperTraining;
	}
	public float getWeeksSpentperTraining() {
		return weeksSpentperTraining;
	}
	public void setWeeksSpentperTraining(float weeksSpentperTraining) {
		this.weeksSpentperTraining = weeksSpentperTraining;
	}
	public float getMonthsSpentperTraining() {
		return monthsSpentperTraining;
	}
	public void setMonthsSpentperTraining(float monthsSpentperTraining) {
		this.monthsSpentperTraining = monthsSpentperTraining;
	}
	public String getCourseEndDate() {
		return courseEndDate;
	}
	public void setCourseEndDate(String courseEndDate) {
		this.courseEndDate = courseEndDate;
	}
}
